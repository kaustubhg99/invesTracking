package com.invesTacking.model;

public class Investments {

	private int inves_Id;
	private String inves_Name;
	private double inves_Market_Cap;
	private int user_Id;
	private double inves_Holdings;
	private double inves_Amount;
	public int getInves_Id() {
		return inves_Id;
	}
	public void setInves_Id(int inves_Id) {
		this.inves_Id = inves_Id;
	}
	public String getInves_Name() {
		return inves_Name;
	}
	public void setInves_Name(String inves_Name) {
		this.inves_Name = inves_Name;
	}
	public double getInves_Market_Cap() {
		return inves_Market_Cap;
	}
	public void setInves_Market_Cap(double inves_Market_Cap) {
		this.inves_Market_Cap = inves_Market_Cap;
	}
	public int getUser_Id() {
		return user_Id;
	}
	public void setUser_Id(int user_Id) {
		this.user_Id = user_Id;
	}
	public double getInves_Holdings() {
		return inves_Holdings;
	}
	public void setInves_Holdings(double inves_Holdings) {
		this.inves_Holdings = inves_Holdings;
	}
	public double getInves_Amount() {
		return inves_Amount;
	}
	public void setInves_Amount(double inves_Amount) {
		this.inves_Amount = inves_Amount;
	}
	
	
}
